/*string.Join:

여러 문자열을 특정 구분자로 연결합니다.
예: string.Join(", ", new string [] { "사과", "바나나", "체리" }) → "사과, 바나나, 체리"


string.Concat:

여러 문자열을 하나로 연결합니다. 구분자 없이 이어붙입니다.
예: string.Concat("Hello", " ", "World") → "Hello World"


string.Trim:

문자열의 시작과 끝에서 지정된 문자(기본적으로 공백)를 제거합니다.
예: " Hello ".Trim() → "Hello"


string.Split:

문자열을 특정 구분자를 기준으로 나눠 배열로 반환합니다.
예: "사과,바나나,체리".Split(',') → ["사과", "바나나", "체리"]*/